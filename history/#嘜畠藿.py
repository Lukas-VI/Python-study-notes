#八个方法:
print("AaAAaaaaaaaAAAAAAAAAAAAAAAA".lower())#全变小
print("aaaaaaaaaaaaaaaaaaAA".upper())        #全变大写
print("1分隔符2分隔符3分隔符4".split("分隔符"))     #用分隔符分开,输出列表
print("1145141919810".count("1"))       #统计1出现的次数
print("进攻A点!".replace("A","D"))               #把字符串中A换成D
print("12321243".center(100,"O"))           #把字符居中放置,用,后面的填充,前面是宽度 
print("python   00000000".strip("0 prt 0"))                  #去除前面字符串两侧中后面括号后面出现的字符
print("*".join("11111111"))         #用前面的分隔符分割后面的字符串
